/* ── Translation engine ──────────────────────────────────────── */

function pvLang() {
  return localStorage.getItem('pv-lang') || 'ja';
}

function pvT(key) {
  const l = pvLang();
  return (PV_I18N[l] && PV_I18N[l][key]) || (PV_I18N['ja'] && PV_I18N['ja'][key]) || key;
}

function pvApplyTranslations() {
  const l = pvLang();
  const dict = PV_I18N[l] || PV_I18N['ja'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = dict[key];
    if (!val) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      // Preserve child elements (icons/svgs) — only replace text nodes
      const hasChildren = el.querySelector('svg, img, button, a');
      if (hasChildren) {
        // Only update the first text node
        for (const node of el.childNodes) {
          if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
            node.textContent = val;
            break;
          }
        }
      } else {
        el.textContent = val;
      }
    }
  });
  document.documentElement.setAttribute('lang', l === 'ja' ? 'ja' : l === 'es' ? 'es' : 'en');
  pvUpdateLangBtn(l);
}

/* ── Switcher UI ─────────────────────────────────────────────── */

const PV_LANG_META = {
  ja: { flag: '🇯🇵', short: 'JP' },
  en: { flag: '🇺🇸', short: 'EN' },
  es: { flag: '🇪🇸', short: 'ES' },
};

function pvUpdateLangBtn(l) {
  const meta = PV_LANG_META[l] || PV_LANG_META['ja'];
  const btn = document.getElementById('pv-lang-btn');
  if (btn) btn.innerHTML = `${meta.flag}<span style="font-size:.72rem;font-weight:700;letter-spacing:.04em">${meta.short}</span><svg width="9" height="9" viewBox="0 0 10 10" fill="none"><path d="M2 3.5l3 3 3-3" stroke="currentColor" stroke-width="1.6" stroke-linecap="round" stroke-linejoin="round"/></svg>`;
}

function pvSetLang(l) {
  localStorage.setItem('pv-lang', l);
  const menu = document.getElementById('pv-lang-menu');
  if (menu) menu.style.display = 'none';
  pvApplyTranslations();
}

function pvToggleLangMenu() {
  const menu = document.getElementById('pv-lang-menu');
  if (!menu) return;
  menu.style.display = (menu.style.display === 'none' || menu.style.display === '') ? 'block' : 'none';
}

/* Build and insert the lang-switcher widget */
